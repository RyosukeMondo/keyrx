//! HTML documentation generator.
//!
//! Generates searchable, syntax-highlighted HTML documentation from the DocRegistry.
//! The generated HTML is self-contained with embedded CSS and JavaScript for
//! search functionality and code highlighting.

use crate::scripting::docs::{
    registry, FunctionDoc, FunctionSignature, ParamDoc, PropertyDoc, TypeDoc,
};
use std::collections::BTreeMap;

/// Generates a complete HTML documentation page for all registered API documentation.
///
/// # Returns
/// A self-contained HTML string with embedded CSS, JavaScript, and all documentation.
/// The HTML includes:
/// - Searchable table of contents
/// - Syntax-highlighted code examples
/// - Responsive design
/// - Client-side search functionality
///
/// # Example
/// ```no_run
/// use keyrx_core::scripting::docs::generators::html::generate_html;
/// use std::fs;
/// let html = generate_html();
/// fs::write("api_docs.html", html).unwrap();
/// ```
pub fn generate_html() -> String {
    let mut output = String::new();

    // HTML header with meta tags and embedded styles
    output.push_str(&html_header());

    // Start body with container
    output.push_str("<body>\n");
    output.push_str("<div class=\"container\">\n");

    // Header section
    output.push_str("<header>\n");
    output.push_str("<h1>Rhai API Documentation</h1>\n");
    output.push_str(
        "<p class=\"subtitle\">Complete API reference for the KeyRx scripting engine</p>\n",
    );
    output.push_str("</header>\n\n");

    // Search bar
    output.push_str("<div class=\"search-container\">\n");
    output.push_str("<input type=\"text\" id=\"search-input\" placeholder=\"Search functions, types, and descriptions...\" />\n");
    output.push_str("<div id=\"search-results\"></div>\n");
    output.push_str("</div>\n\n");

    // Collect modules
    let modules = collect_modules();

    // Table of contents with module list
    output.push_str("<nav class=\"toc\">\n");
    output.push_str("<h2>Table of Contents</h2>\n");
    output.push_str("<ul>\n");

    for module_name in modules.keys() {
        let anchor = module_name.to_lowercase().replace(' ', "-");
        output.push_str(&format!(
            "<li><a href=\"#{}\">{}</a></li>\n",
            anchor,
            escape_html(module_name)
        ));
    }

    output.push_str("</ul>\n");
    output.push_str("</nav>\n\n");

    // Main content
    output.push_str("<main>\n");

    for (module_name, module) in modules {
        generate_module_html(&mut output, &module_name, &module);
    }

    output.push_str("</main>\n");

    // Footer
    output.push_str("<footer>\n");
    output.push_str("<p>Generated by KeyRx Documentation Generator</p>\n");
    output.push_str("</footer>\n");

    output.push_str("</div>\n"); // Close container

    // Embedded JavaScript for search and highlighting
    output.push_str(&html_scripts());

    output.push_str("</body>\n");
    output.push_str("</html>\n");

    output
}

/// Collects all documentation organized by module.
fn collect_modules() -> BTreeMap<String, ModuleData> {
    let mut modules = BTreeMap::new();

    // Collect functions by module
    for func in registry::all_functions() {
        let module_name = func.module.clone();
        let entry = modules
            .entry(module_name.clone())
            .or_insert_with(|| ModuleData::new(module_name));
        entry.functions.push(func);
    }

    // Collect types by module
    for type_doc in registry::all_types() {
        let module_name = type_doc.module.clone();
        let entry = modules
            .entry(module_name.clone())
            .or_insert_with(|| ModuleData::new(module_name));
        entry.types.push(type_doc);
    }

    // Add module-level documentation if available
    for module_doc in registry::all_modules() {
        if let Some(entry) = modules.get_mut(&module_doc.name) {
            entry.description = Some(module_doc.description);
        }
    }

    modules
}

/// Temporary structure to hold module data during generation.
struct ModuleData {
    description: Option<String>,
    functions: Vec<FunctionDoc>,
    types: Vec<TypeDoc>,
}

impl ModuleData {
    fn new(_name: String) -> Self {
        Self {
            description: None,
            functions: Vec::new(),
            types: Vec::new(),
        }
    }
}

/// Generates HTML for a single module.
fn generate_module_html(output: &mut String, module_name: &str, module: &ModuleData) {
    let anchor = module_name.to_lowercase().replace(' ', "-");

    output.push_str(&format!("<section class=\"module\" id=\"{}\">\n", anchor));
    output.push_str(&format!("<h2>{}</h2>\n", escape_html(module_name)));

    // Module description
    if let Some(description) = &module.description {
        output.push_str(&format!(
            "<p class=\"module-desc\">{}</p>\n",
            escape_html(description)
        ));
    }

    // Types section
    if !module.types.is_empty() {
        output.push_str("<div class=\"types-section\">\n");
        output.push_str("<h3>Types</h3>\n");
        for type_doc in &module.types {
            generate_type_html(output, type_doc);
        }
        output.push_str("</div>\n");
    }

    // Functions section
    if !module.functions.is_empty() {
        output.push_str("<div class=\"functions-section\">\n");
        output.push_str("<h3>Functions</h3>\n");
        for func in &module.functions {
            generate_function_html(output, func);
        }
        output.push_str("</div>\n");
    }

    output.push_str("</section>\n");
}

/// Generates HTML for a single type.
fn generate_type_html(output: &mut String, type_doc: &TypeDoc) {
    output.push_str("<div class=\"type-doc\">\n");
    output.push_str(&format!(
        "<h4><code>{}</code></h4>\n",
        escape_html(&type_doc.name)
    ));
    output.push_str(&format!("<p>{}</p>\n", escape_html(&type_doc.description)));

    // Version info
    if let Some(since) = &type_doc.since {
        output.push_str(&format!(
            "<p class=\"version\"><em>Since: {}</em></p>\n",
            escape_html(since)
        ));
    }

    // Properties
    if !type_doc.properties.is_empty() {
        output.push_str("<div class=\"properties\">\n");
        output.push_str("<strong>Properties:</strong>\n");
        output.push_str("<ul>\n");
        for prop in &type_doc.properties {
            generate_property_html(output, prop);
        }
        output.push_str("</ul>\n");
        output.push_str("</div>\n");
    }

    // Methods
    if !type_doc.methods.is_empty() {
        output.push_str("<div class=\"methods\">\n");
        output.push_str("<strong>Methods:</strong>\n");
        output.push_str("<ul>\n");
        for method in &type_doc.methods {
            generate_method_html(output, method);
        }
        output.push_str("</ul>\n");
        output.push_str("</div>\n");
    }

    // Constructors
    if !type_doc.constructors.is_empty() {
        output.push_str("<div class=\"constructors\">\n");
        output.push_str("<strong>Constructors:</strong>\n");
        output.push_str("<ul>\n");
        for constructor in &type_doc.constructors {
            generate_method_html(output, constructor);
        }
        output.push_str("</ul>\n");
        output.push_str("</div>\n");
    }

    // Examples
    if !type_doc.examples.is_empty() {
        output.push_str("<div class=\"examples\">\n");
        output.push_str("<strong>Examples:</strong>\n");
        for example in &type_doc.examples {
            output.push_str("<pre><code class=\"language-rhai\">");
            output.push_str(&escape_html(example));
            output.push_str("</code></pre>\n");
        }
        output.push_str("</div>\n");
    }

    output.push_str("</div>\n");
}

/// Generates HTML for a property.
fn generate_property_html(output: &mut String, prop: &PropertyDoc) {
    let readonly = if prop.readonly {
        " <span class=\"badge readonly\">read-only</span>"
    } else {
        ""
    };
    output.push_str(&format!(
        "<li><code>{}</code>: <code>{}</code>{} - {}</li>\n",
        escape_html(&prop.name),
        escape_html(&prop.type_name),
        readonly,
        escape_html(&prop.description)
    ));
}

/// Generates HTML for a method (compact format for type methods).
fn generate_method_html(output: &mut String, method: &FunctionDoc) {
    let signature = format_signature(&method.signature);
    output.push_str(&format!(
        "<li><code>{}</code> - {}</li>\n",
        escape_html(&signature),
        escape_html(&method.description)
    ));
}

/// Generates HTML for a function.
fn generate_function_html(output: &mut String, func: &FunctionDoc) {
    output.push_str("<div class=\"function-doc\">\n");

    // Function signature
    let params = func
        .signature
        .params
        .iter()
        .map(|(name, type_name)| format!("{}: {}", name, type_name))
        .collect::<Vec<_>>()
        .join(", ");

    let return_type = func
        .signature
        .return_type
        .as_ref()
        .map(|t| format!(" -&gt; {}", t))
        .unwrap_or_default();

    let signature = format!("{}({}){}", func.name, params, return_type);
    output.push_str(&format!(
        "<h4><code>{}</code></h4>\n",
        escape_html(&signature)
    ));

    // Deprecation warning
    if let Some(deprecated) = &func.deprecated {
        output.push_str(&format!(
            "<div class=\"deprecated\">⚠️ DEPRECATED: {}</div>\n",
            escape_html(deprecated)
        ));
    }

    // Description
    output.push_str(&format!("<p>{}</p>\n", escape_html(&func.description)));

    // Parameters
    if !func.parameters.is_empty() {
        output.push_str("<div class=\"parameters\">\n");
        output.push_str("<strong>Parameters:</strong>\n");
        output.push_str("<ul>\n");
        for param in &func.parameters {
            generate_param_html(output, param);
        }
        output.push_str("</ul>\n");
        output.push_str("</div>\n");
    }

    // Return value
    if let Some(returns) = &func.returns {
        output.push_str(&format!(
            "<div class=\"returns\"><strong>Returns:</strong> <code>{}</code> - {}</div>\n",
            escape_html(&returns.type_name),
            escape_html(&returns.description)
        ));
    }

    // Notes
    if let Some(notes) = &func.notes {
        output.push_str(&format!(
            "<div class=\"notes\"><strong>Notes:</strong> {}</div>\n",
            escape_html(notes)
        ));
    }

    // Examples
    if !func.examples.is_empty() {
        output.push_str("<div class=\"examples\">\n");
        output.push_str("<strong>Examples:</strong>\n");
        for example in &func.examples {
            output.push_str("<pre><code class=\"language-rhai\">");
            output.push_str(&escape_html(example));
            output.push_str("</code></pre>\n");
        }
        output.push_str("</div>\n");
    }

    // Version info
    if let Some(since) = &func.since {
        output.push_str(&format!(
            "<p class=\"version\"><em>Since: {}</em></p>\n",
            escape_html(since)
        ));
    }

    output.push_str("</div>\n");
}

/// Generates HTML for a parameter.
fn generate_param_html(output: &mut String, param: &ParamDoc) {
    let optional = if param.optional {
        " <span class=\"badge optional\">optional</span>"
    } else {
        ""
    };
    let default = if let Some(default) = &param.default {
        format!(" = <code>{}</code>", escape_html(default))
    } else {
        String::new()
    };

    output.push_str(&format!(
        "<li><code>{}</code>: <code>{}</code>{}{} - {}</li>\n",
        escape_html(&param.name),
        escape_html(&param.type_name),
        optional,
        default,
        escape_html(&param.description)
    ));
}

/// Formats a function signature for display.
fn format_signature(sig: &FunctionSignature) -> String {
    let params = sig
        .params
        .iter()
        .map(|(name, type_name)| format!("{}: {}", name, type_name))
        .collect::<Vec<_>>()
        .join(", ");

    let return_type = sig
        .return_type
        .as_ref()
        .map(|t| format!(" -> {}", t))
        .unwrap_or_default();

    format!("fn({}){}", params, return_type)
}

/// Escapes HTML special characters.
fn escape_html(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
}

