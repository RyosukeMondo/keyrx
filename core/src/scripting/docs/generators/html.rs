/// Returns the HTML scripts for search functionality.
fn html_scripts() -> String {
    r#"<script>
        // Search functionality
        const searchInput = document.getElementById('search-input');
        const searchResults = document.getElementById('search-results');

        // Build search index from all content
        const buildSearchIndex = () => {
            const index = [];

            // Index all functions
            document.querySelectorAll('.function-doc').forEach(func => {
                const heading = func.querySelector('h4');
                const description = func.querySelector('p');
                if (heading && description) {
                    const module = func.closest('.module');
                    const moduleName = module ? module.querySelector('h2').textContent : '';

                    index.push({
                        type: 'function',
                        name: heading.textContent.trim(),
                        description: description.textContent.trim(),
                        module: moduleName,
                        element: func
                    });
                }
            });

            // Index all types
            document.querySelectorAll('.type-doc').forEach(type => {
                const heading = type.querySelector('h4');
                const description = type.querySelector('p');
                if (heading && description) {
                    const module = type.closest('.module');
                    const moduleName = module ? module.querySelector('h2').textContent : '';

                    index.push({
                        type: 'type',
                        name: heading.textContent.trim(),
                        description: description.textContent.trim(),
                        module: moduleName,
                        element: type
                    });
                }
            });

            return index;
        };

        const searchIndex = buildSearchIndex();

        // Search function
        const search = (query) => {
            if (!query || query.length < 2) {
                searchResults.classList.remove('show');
                return;
            }

            const lowerQuery = query.toLowerCase();
            const results = searchIndex.filter(item => {
                return item.name.toLowerCase().includes(lowerQuery) ||
                       item.description.toLowerCase().includes(lowerQuery) ||
                       item.module.toLowerCase().includes(lowerQuery);
            });

            displaySearchResults(results);
        };

        // Display search results
        const displaySearchResults = (results) => {
            searchResults.innerHTML = '';

            if (results.length === 0) {
                searchResults.innerHTML = '<div class="search-result-item">No results found</div>';
                searchResults.classList.add('show');
                return;
            }

            results.slice(0, 10).forEach(result => {
                const item = document.createElement('div');
                item.className = 'search-result-item';
                item.innerHTML = `
                    <strong>${result.name}</strong><br>
                    <small>${result.module} • ${result.type}</small><br>
                    <small style="color: #6b7280;">${result.description.substring(0, 100)}...</small>
                `;

                item.addEventListener('click', () => {
                    result.element.scrollIntoView({ behavior: 'smooth', block: 'center' });
                    result.element.style.backgroundColor = 'var(--primary-color)';
                    result.element.style.transition = 'background-color 0.3s';
                    setTimeout(() => {
                        result.element.style.backgroundColor = '';
                    }, 1000);
                    searchResults.classList.remove('show');
                    searchInput.value = '';
                });

                searchResults.appendChild(item);
            });

            searchResults.classList.add('show');
        };

        // Event listeners
        searchInput.addEventListener('input', (e) => {
            search(e.target.value);
        });

        searchInput.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                searchResults.classList.remove('show');
                searchInput.value = '';
            }
        });

        // Close search results when clicking outside
        document.addEventListener('click', (e) => {
            if (!searchInput.contains(e.target) && !searchResults.contains(e.target)) {
                searchResults.classList.remove('show');
            }
        });

        // Syntax highlighting for code blocks (simple implementation)
        document.querySelectorAll('code.language-rhai').forEach(block => {
            const text = block.textContent;
            const keywords = ['let', 'const', 'fn', 'if', 'else', 'for', 'while', 'return', 'true', 'false', 'null'];

            let highlighted = text;
            keywords.forEach(keyword => {
                const regex = new RegExp(`\\b(${keyword})\\b`, 'g');
                highlighted = highlighted.replace(regex, '<span style="color: #7c3aed; font-weight: bold;">$1</span>');
            });

            // Highlight strings
            highlighted = highlighted.replace(/(".*?")/g, '<span style="color: #059669;">$1</span>');
            highlighted = highlighted.replace(/('.*?')/g, '<span style="color: #059669;">$1</span>');

            // Highlight comments
            highlighted = highlighted.replace(/(\/\/.*$)/gm, '<span style="color: #6b7280; font-style: italic;">$1</span>');

            // Highlight numbers
            highlighted = highlighted.replace(/\b(\d+)\b/g, '<span style="color: #dc2626;">$1</span>');

            block.innerHTML = highlighted;
        });
    </script>
"#.to_string()
}

