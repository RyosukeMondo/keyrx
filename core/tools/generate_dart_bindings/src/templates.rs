    fn test_ffi_exception_class() {
        assert!(FFI_EXCEPTION_CLASS.contains("class FfiException"));
        assert!(FFI_EXCEPTION_CLASS.contains("final String message"));
        assert!(FFI_EXCEPTION_CLASS.contains("FfiException(this.message)"));
    }
