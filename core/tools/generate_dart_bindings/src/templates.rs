/// Template for wrapper function with error handling and string return
///
/// Placeholders:
/// - `{{dart_function_name}}` - Dart function name (camelCase)
/// - `{{dart_return_type}}` - Dart return type (e.g., String, void)
/// - `{{dart_params_signature}}` - Dart parameter signature (e.g., String json, int count)
/// - `{{param_conversions}}` - Code to convert Dart params to FFI (e.g., final jsonPtr = json.toNativeUtf8();)
/// - `{{ffi_call_args}}` - Arguments to pass to FFI function
/// - `{{param_frees}}` - Code to free allocated parameters
/// - `{{lookup_name}}` - Name of the function pointer variable
/// - `{{result_conversion}}` - Code to convert result to Dart type
/// - `{{free_result}}` - Code to free the result pointer (if needed)
pub const WRAPPER_FUNCTION_WITH_ERROR_TEMPLATE: &str = r#"/// {{doc_comment}}
{{dart_return_type}} {{dart_function_name}}({{dart_params_signature}}) {
  final errorPtr = calloc<Pointer<Utf8>>();
  try {
{{param_conversions}}
    final resultPtr = _{{lookup_name}}({{ffi_call_args}});
{{param_frees}}
    if (errorPtr.value.address != 0) {
      final error = errorPtr.value.toDartString();
      calloc.free(errorPtr.value);
      throw FfiException(error);
    }
{{result_conversion}}
  } finally {
    calloc.free(errorPtr);
  }
}"#;

