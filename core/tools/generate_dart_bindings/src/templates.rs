//! Code generation templates for Dart FFI bindings
//!
//! This module provides string templates and rendering utilities for generating
//! Dart FFI function signatures, wrappers, and model classes.

use std::collections::HashMap;

/// Template context for placeholder replacement
pub type TemplateContext = HashMap<String, String>;

/// Create a new template context
pub fn context() -> TemplateContext {
    HashMap::new()
}

/// Render a template by replacing placeholders with context values.
///
/// Placeholders use the format `{{key}}` and are replaced with the
/// corresponding value from the context.
pub fn render(template: &str, ctx: &TemplateContext) -> String {
    let mut result = template.to_string();
    for (key, value) in ctx {
        let placeholder = format!("{{{{{}}}}}", key);
        result = result.replace(&placeholder, value);
    }
    result
}

